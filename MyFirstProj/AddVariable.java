public class AddVariable{

     public static void main(String []args){
         int x = 10;
         int y = 13;
         int z = x+y;
        System.out.println(z);
        
        float x1 = 10;
        float y1 = 13;
        float z1 = x+y;
        System.out.println(z1);
        
        double x2 = 10.0;
        double y2 = 13.0;
        double z2 = x+y;
        System.out.println(z2);
     }
}